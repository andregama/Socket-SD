    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Socket cli = new Socket("localhost",20000);
            cli.setKeepAlive(true);
            ObjectOutputStream output = new ObjectOutputStream(cli.getOutputStream());

            Player player = new Player();
            player.setId(Integer.parseInt(jTextField1.getText()));

            Requisicao req = new Requisicao();
            req.setMessageType(Requisicao.GET_PLAYER);
            req.setPlayerContent(player);

            output.writeObject(req);

            ObjectInputStream input = new ObjectInputStream(cli.getInputStream());
            Resposta rep = (Resposta)input.readObject();
            if (rep.getMessageStatus() == Resposta.PLAYER_NOT_FOUND){
                JOptionPane.showMessageDialog(this,"NÃ£o foi encontrado player");
            }
            else{
                JOptionPane.showMessageDialog(this, "Encontrado!!!");
                jTextField2.setText(rep.getPlayerContent().getName());
                jTextField3.setText(rep.getPlayerContent().getEmail());
                jTextField4.setText(rep.getPlayerContent().getScore()+"");
            }

            output.close();
            input.close();
            cli.close();

        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Socket client = new Socket("localhost", 20000);
            client.setKeepAlive(true);
            ObjectOutputStream output = new ObjectOutputStream(client.getOutputStream());

            Requisicao req = new Requisicao();
            Player player  = new Player();
            player.setId(Integer.parseInt(jTextField1.getText()));
            player.setName(jTextField2.getText());
            player.setEmail(jTextField3.getText());
            player.setScore(Float.parseFloat(jTextField4.getText()));

            req.setMessageType(Requisicao.NEW_PLAYER);
            req.setPlayerContent(player);

            output.writeObject(req);
            // JOptionPane.showMessageDialog(this, "Sending message");

            ObjectInputStream  input  = new ObjectInputStream(client.getInputStream());
            Resposta rep = (Resposta)input.readObject();

            //JOptionPane.showMessageDialog(this, "Message Received");
            if (rep.getMessageStatus() == Resposta.NEW_PLAYER_OK){
                JOptionPane.showMessageDialog(this,"Player Cadastrado com sucesso!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Deu ruim.");
            }

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");

            output.close();
            input.close();
            client.close();

        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

